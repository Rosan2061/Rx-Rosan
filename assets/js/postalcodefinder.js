    const provinceSelect = document.getElementById('province');
    const districtSelect = document.getElementById('district');
    const streetSelect = document.getElementById('street');
    const mainSearchInput = document.getElementById('main-search');
    const resultDiv = document.getElementById('result');
    const postalCodeSpan = document.getElementById('postalCode');

    function displayPostalCode(postalCode) {
        postalCodeSpan.textContent = postalCode;
        resultDiv.classList.remove('hidden');
    }

    function resetDropdowns() {
        districtSelect.innerHTML = '<option value="">-- Select District --</option>';
        streetSelect.innerHTML = '<option value="">-- Select Street --</option>';
        streetSelect.disabled = true;
        resultDiv.classList.add('hidden');
    }

    provinceSelect.addEventListener('change', () => {
        const province = provinceSelect.value;
        resetDropdowns();

        if (province && postalCodes[province]) {
            districtSelect.disabled = false;
            Object.keys(postalCodes[province]).forEach(district => {
                const option = document.createElement('option');
                option.value = district;
                option.textContent = district;
                districtSelect.appendChild(option);
            });
        } else {
            districtSelect.disabled = true;
        }
    });

    districtSelect.addEventListener('change', () => {
        const province = provinceSelect.value;
        const district = districtSelect.value;

        streetSelect.innerHTML = '<option value="">-- Select Street --</option>';
        streetSelect.disabled = true;

        if (province && district && postalCodes[province][district]) {
            const streets = postalCodes[province][district].streets;

            streets.forEach(street => {
                const option = document.createElement('option');
                option.value = street.name;
                option.textContent = street.name;
                streetSelect.appendChild(option);
            });
            streetSelect.disabled = false;
        }
    });

    streetSelect.addEventListener('change', () => {
        const province = provinceSelect.value;
        const district = districtSelect.value;
        const street = streetSelect.value;

        if (province && district && street) {
            const selectedStreet = postalCodes[province][district].streets.find(s => s.name === street);
            if (selectedStreet) {
                displayPostalCode(selectedStreet.postalCode);
            }
        }
    });

    mainSearchInput.addEventListener('input', () => {
        const query = mainSearchInput.value.toLowerCase();
        resetDropdowns();

        let found = false;

        // Search through all provinces, districts, and streets
        for (let province in postalCodes) {
            for (let district in postalCodes[province]) {
                // Check if the district matches
                if (district.toLowerCase().includes(query)) {
                    provinceSelect.value = province;
                    provinceSelect.dispatchEvent(new Event('change'));
                    districtSelect.value = district;
                    districtSelect.dispatchEvent(new Event('change'));
                    found = true;
                    break;
                }

                // Check if the street matches within the district
                const streets = postalCodes[province][district].streets;
                const matchedStreet = streets.find(street => street.name.toLowerCase().includes(query));

                if (matchedStreet) {
                    provinceSelect.value = province;
                    provinceSelect.dispatchEvent(new Event('change'));
                    districtSelect.value = district;
                    districtSelect.dispatchEvent(new Event('change'));
                    streetSelect.value = matchedStreet.name;
                    displayPostalCode(matchedStreet.postalCode);
                    found = true;
                    break;
                }
            }

            if (found) break;
        }
    });

